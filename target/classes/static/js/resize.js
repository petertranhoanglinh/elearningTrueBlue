function resizeGridRightShow(){
	var width = $("#content").width() - $("#search").width() + 60;
	var prgId = oldUrl;
	if(prgId == ''){
		prgId = 'home';
	}
	
	for(var i =0; i< resizeAUIGrid.length; i++){
		if(resizeAUIGrid[i].id.indexOf(prgId) != -1){
			var gird_width = width - 83;
			
			if(resizeAUIGrid[i].size){
				gird_width = gird_width * resizeAUIGrid[i].size / 100
			}
			
			if(resizeAUIGrid[i].padding){
				gird_width -= resizeAUIGrid[i].padding;
			}
			
			$(resizeAUIGrid[i].id).css("width", gird_width);
			AUIGrid.resize(resizeAUIGrid[i].id);
		}
	}	
}

